from turtle import *

#we want to paint a house
#step 1: draw a square
shape("turtle")
speed(5)
width(7)
color("blue")

forward(200)
left(90)
forward(200)
left(90)
forward(200)
left(90)
forward(200)
left(90)

#step 2 draw a door
begin_fill()
forward(70)
color("green")

left(90)
forward(120)
right(90)
forward(60)
right(90)
forward(120)
end_fill()
penup()
goto(200, 200)
pendown()
#step 3 draw a roof
color("red")
begin_fill()
right(150)
forward(200)
left(120)
forward(200)
end_fill()
goto(200, 200)
#step 4 draw some windows
color("blue")
right(330)
forward(60)
right(90)
color("white")
forward(30)
right(90)
color("brown")
begin_fill()
forward(40)
left(90)
forward(30)
left(90)
forward(40)
left(90)
forward(30)
end_fill()
#step 5 drawing second window

color("white")
forward(30)
left(90)
color("blue")
forward(60)
color("blue")
left(90)
forward(200)
left(90)

forward(60)
color("blue")
left(90)
color("brown")
begin_fill()
forward(30)
left(90)
forward(40)
right(90)
forward(30)
right(90)
forward(40)
right(90)
forward(40)
end_fill()

exitonclick()